$(document).ready(function () {
    var owl = $('.owl-carousel');
    owl.owlCarousel({

        margin: 140,
        nav: false,
        loop: true,
        interval: 2000,
        keyboard: false,
        pause: 'hover',
        ride: 'carousel',
        autoplay: true,
        interval: 2000,



        responsive: {
            0: {
                items: 1
            },
            600: {
                items: 3
            },
            1000: {
                items: 5
            }
        }
    })
})

